    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String username = txtKorisnickoIme.getText().trim();
                String password = String.valueOf(txtPassword.getPassword());
                String url = txtURL.getText().trim();
                try {
                   if(!validacija(url, username).equals("")){
                       throw new Exception(validacija(url, username));
                       
                   }
                    Properties properties = new Properties();
                    properties.load(new FileInputStream("config/dbconfig.properties"));
                    properties.setProperty("username", username);
                    properties.setProperty("password", password);
                    properties.setProperty("url", url);
                    properties.store(new FileOutputStream("config/dbconfig.properties"), null);
                    JOptionPane.showMessageDialog(this, "Podešavanja baze su sačuvana!", "Sačuvano", JOptionPane.INFORMATION_MESSAGE);
                } catch(Exception e) {
                   
                    JOptionPane.showMessageDialog(this, "Greška! " + e.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
                }
    }//GEN-LAST:event_btnSacuvajActionPerformed

