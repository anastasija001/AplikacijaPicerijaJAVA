    private String validacija(String url, String username)  {
       String poruka="";
        
        if(url.equals("")){
           poruka+="URL ne sme biti prazan! \n";
        }
        if(username.equals("")){
           poruka+="Username ne sme biti prazno!";
        }
        return poruka;
    }

   

    
   


